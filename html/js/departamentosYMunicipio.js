$("#departamento").change(function(){
    llenarComboMunicipio($('select[id=departamento]').val());
});

function llenarComboMunicipio(idDepartamento){
	var id = idDepartamento;
	var llenarComboMunicipio ="";
	if (id != 0) {
		for (var i = 1; i < municipioCombo[id].length; i++) {
			llenarComboMunicipio += "<option value='"+i+"'>"+municipioCombo[id][i]+"</option>";
		}
		document.getElementById("municipio").innerHTML= llenarComboMunicipio;
	}else{
		llenarComboMunicipio += "<option value='0'>Selecciona departamento</option>";
		document.getElementById("municipio").innerHTML= llenarComboMunicipio;
	}
};


